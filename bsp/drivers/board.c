/*
********************************************************************************
*Function    : SystemClock_ReConfig
*Description :
*Input       :
*Output      :
*Return      :
*Others      :
********************************************************************************
*/
void SystemClock_ReConfig(uint8_t mode)
{
    SystemClock_MSI_ON();
    
    switch(mode){
       case PM_RUN_MODE_HIGH_SPEED:
         break;
       case PM_RUN_MODE_NORMAL_SPEED:
        
         SystemClock_80M();
         break;
       case PM_RUN_MODE_MEDIUM_SPEED:
         SystemClock_24M();
         break;
       case PM_RUN_MODE_LOW_SPEED:
         SystemClock_2M();
         break;
         default:
           break;
    }
}
